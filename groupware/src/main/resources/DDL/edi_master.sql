CREATE TABLE EDI_MASTER(
  EDI_CODE VARCHAR2(20) PRIMARY KEY,
  INP_EMP_CODE VARCHAR2(10) NOT NULL,
  EDI_TITLE VARCHAR2(50) NOT NULL,
  EDI_CONTENTS VARCHAR2(1000),
  EDI_TYPE VARCHAR2(4) NOT NULL,
  EDI_STATUS VARCHAR2(1) NOT NULL,
  FILE_NAME VARCHAR2(500),
  INP_DATE DATE DEFAULT SYSDATE,
  UPDATE_DATE DATE DEFAULT SYSDATE
);

CREATE SEQUENCE SEQ_EDI_MASTER
    START WITH 1
    INCREMENT BY 1
    MAXVALUE 9999999
    MINVALUE 1
    NOCYCLE;